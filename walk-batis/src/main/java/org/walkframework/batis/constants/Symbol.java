package org.walkframework.batis.constants;

/**
 * 定义常量符号
 * 
 * @author shf675
 *
 */
public interface Symbol {
    /**
     * @Fields A_SPACE : 一个空格 " "
     */
    String A_SPACE = " ";

    /**
     * @Fields CROSS_BAR : 小横杠 "-"
     */
    String CROSS_BAR = "-";
    /**
     * @Fields COLON : 冒号 ":"
     */
    String COLON = ":";

    /**
     * @Fields DOT : 点号 "."
     */
    String DOT = ".";

    /**
     * @Fields FORWARD_SLASH : 正斜杠"/"
     */
    String FORWARD_SLASH = "/";

    /**
     * @Fields BACK_SLASH : 反斜杠"\"
     */
    String BACK_SLASH = "\\";

    /**
     * @Fields UNDERLINE : 下划线"_"
     */
    String UNDERLINE = "_";

    /**
     * @Fields ERECTLINE :竖线"|"
     */
    String ERECTLINE = "\\|";

    /**
     * @Fields AND : 连接符号"&"
     */
    String AND = "&";

    /**
     * @Fields SEMICOLON : 分号";"
     */
    String SEMICOLON = ";";

    /**
     * @Fields TRUE_CHAR : 为真的字符串"1"
     */
    String TRUE_CHAR = "1";
    /**
     * @Fields FALSE_CHAR : 为真的字符串"0"
     */
    String FALSE_CHAR = "0";

    /**
     * @Fields OPEN_BRACKET : "["　open bracket 左方括号
     */
    String OPEN_BRACKET = "[";

    /**
     * @Fields CLOSE_BRACKET : "]"　close bracket 右方括号
     */
    String CLOSE_BRACKET = "]";

    /**
     * @Fields LEFT_BRACKET : "<"　左尖括号
     */
    String LEFT_BRACKET = "<";

    /**
     * @Fields RIGHT_BRACKET : ">"　右尖括号
     */
    String RIGHT_BRACKET = ">";
}
